// AREA OF CIRCLE
#include <stdio.h>
int main(){
    int height, radius;
    printf("the radius of a cylinder is \n", radius);
    scanf("%d",&radius);
    float pi=3.14;

    printf("the height of a cylinder is \n", height);
    scanf("%d",&height);
    printf("the area of a circle is %f\n",pi*radius*radius);

    printf("Volume of this cylinder is %f\n", pi * radius * radius * height);

    return 0;
}



